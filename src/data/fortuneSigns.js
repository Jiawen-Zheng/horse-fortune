/**
 * 根据签号获取签文
 * @param {number} signId - 签号 (1-200)
 * @returns {object} 签文对象
 */
export function getFortuneSignById(signId) {
  return fortuneSigns.find(sign => sign.id === signId) || fortuneSigns[0];
}

/**
 * 获取所有上上签
 * @returns {array} 上上签数组
 */
export function getFortuneSupremeSigns() {
  return fortuneSigns.filter(sign => sign.level === 'supreme');
}

/**
 * 获取所有上签
 * @returns {array} 上签数组
 */
export function getFortuneExcellentSigns() {
  return fortuneSigns.filter(sign => sign.level === 'excellent');
}

/**
 * 随机获取一支签
 * @returns {object} 随机签文
 */
export function getRandomFortuneSign() {
  const randomId = Math.floor(Math.random() * 200) + 1;
  return getFortuneSignById(randomId);
}